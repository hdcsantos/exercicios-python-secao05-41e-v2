def soma(n1, n2):
    return n1 + n2


def subtracao(n1, n2):
    return n1 - n2


def multiplicacao(n1, n2):
    return n1 * n2


def divisao(n1, n2):
    return n1 / n2


def produto(n1, n2):
    return n1 % n2


def raiz_quadrada(n1):
    return n1 ** 0.5


def exp_cubo(n1):
    return n1 ** 3


def exp_quadrada(n1):
    return n1 ** 2
